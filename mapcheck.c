#include "./lib/cub3D.h"

int	mapcheck(char *map)
{
	int	i;

	i = 0;
	while (map[i])
		i++;
	if (*map == 0 || i-- < 5)
		return (0);
	if (map[i] == 'b' && map[i - 1] == 'u' && map[i - 2] == 'c' && map[i - 3]\
			== '.')
		return (1);
	else
		return (0);
}
